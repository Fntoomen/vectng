Map::Map() {
  dirt = TextureManager::LoadTexture("Tiles/dirt.png");
  grass = TextureManager::LoadTexture("Tiles/grass.png");
  water = TextureManager::LoadTexture("Tiles/liquidWater.png");

  LoadMap(lvl1);

  src.x = 0;
  src.y = 0;
  src.h = 70;
  src.w = 70;

  dest.x = 0;
  dest.y = 0;
  dest.h = 70;
  dest.w = 70;
}

void Map::LoadMap(unsigned char arr[32][32]) {
  for (unsigned char row = 0; row < 32; row++) {
    for (unsigned char column = 0; column < 32; column++) {
      map[row][column] = arr[row][column];
    }
  }
}
void Map::DrawMap() {
  unsigned char type = 0;
  for (unsigned char row = 0; row < 32; row++) {
    for (unsigned char column = 0; column < 32; column++) {
      type = map[row][column];

      dest.x = column * 70;
      dest.y = row * 70;

      TextureManager::Draw(water, src, dest);

      switch (type) {
      case 0:
        TextureManager::Draw(grass, src, dest);
        break;
      case 2:
        TextureManager::Draw(dirt, src, dest);
        break;
      default:
        break;
      }
    }
  }
}
