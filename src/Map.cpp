Map::Map() {
  dirt = TextureManager::LoadTexture("assets/Tiles/dirt.png");
  grass = TextureManager::LoadTexture("assets/Tiles/grass.png");
  water = TextureManager::LoadTexture("assets/Tiles/liquidWater.png");

  LoadMap(lvl1);

  src.x = 5;
  src.y = 5;
  src.h = 60;
  src.w = 60;

  dest.x = 0;
  dest.y = 0;
  dest.h = 68;
  dest.w = 68;
}

Map::~Map() {
  SDL_DestroyTexture(dirt);
  SDL_DestroyTexture(grass);
  SDL_DestroyTexture(water);
}

void Map::LoadMap(const uint_fast32_t arr[32][32]) {
  std::memcpy(map, arr, sizeof(map));
}

void Map::DrawMap() {
  uint_fast32_t type = 0;
  for (uint_fast32_t row = 0; row < 32; row++) {
    for (uint_fast32_t column = 0; column < 32; column++) {
      type = map[row][column];
      dest.x = column * 70;
      dest.y = row * 70;
      TextureManager::Draw(water, src, dest);
      switch (type) {
      case 0:
        TextureManager::Draw(grass, src, dest);
        break;
      case 1:
        TextureManager::Draw(water, src, dest);
      case 2:
        TextureManager::Draw(dirt, src, dest);
        break;
      default:
        break;
      }
    }
  }
}
